# I hear these called conditional statements.
# I hear them also called control-flow statements.


x = 5

if x == 0:
    print('0 is true')
elif x == 1:
    print('1 is true')
elif x == 2:
    print('2 is true')
elif x == 3:
    print('3 is true')
elif x == 4:
    print('4 is true')
else:
    print('neither true')
